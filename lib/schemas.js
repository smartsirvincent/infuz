export function getPostType(typeKey) {
  const t = POST_TYPES[typeKey];
  if (!t) throw new Error(`Unknown post type: ${typeKey}`);
  return t;
}

export function allTypeKeys() {
  return Object.keys(POST_TYPES);
}

export function typeMenu() {
  return Object.entries(POST_TYPES).map(([k, v]) => ({
    key: k,
    label: v.label,
    hint: v.promptHint,
  }));
}
